#include "ft_printf.h"

int	ft_printun(unsigned int nb)
{
	int	i;

	i = ft_len(nb);
	if (nb > 9)
	{
		ft_printun(nb / 10);
	}
	ft_putchar((nb % 10) + '0');
	return (i);
}
