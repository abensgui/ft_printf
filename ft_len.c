#include"ft_printf.h"

int	ft_len(long a)
{
	int	i;

	i = 0;
	if (a < 0)
		i++;
	if (a == 0)
		i++;
	while (a != 0)
	{
		a /= 10;
		i++;
	}
	return (i);
}
